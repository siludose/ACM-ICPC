#include<iostream>
using namespace std;
const int maxn=100+5;
int n,m;
int a[maxn],c[maxn];
int main()
{
    while(scanf("%d%d",&n,&m)!=EOF)
    {
        for(int i=0;i<n;i++)       
            scanf("%d",&a[i]);
        for(int i=0;i<n;i++)
            scanf("%d",&c[i]);
        
    }
    return 0;
}